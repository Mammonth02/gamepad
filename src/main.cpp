// Базовые функции и типы Arduino: setup(), loop(), Serial и т.д.
#include <Arduino.h>
// Логика подключения к Wi‑Fi и отправки данных.
#include "wifi.h"
// Чтение координат джойстика и кнопок.
#include "input.h"
// Отрисовка интерфейса на дисплее.
#include "ui.h"

// setup() вызывается один раз при старте платы.
void setup() {
  // Открываем последовательный порт для отладки в Serial Monitor.
  Serial.begin(9600);

  // Инициализируем экран.
  initUI();
  // Инициализируем входные данные от контроллера.
  initInput();
  // сканируем Wi‑Fi.
  scanWIFI(); 
}

// loop() выполняется постоянно по кругу, пока плата включена.
void loop() {
  // Выполняем поиск ПК по сети и отправляем ему данные геймпада.
  handleWiFi();   // discovery + отправка
  // Считываем новые данные от джойстика и кнопок.
  handleInput();  // чтение джойстика
  // Обновляем изображение на экране.
  drawUI();
}
